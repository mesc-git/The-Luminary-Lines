<script>
document.addEventListener("DOMContentLoaded", function () {
  const filterButtons = document.querySelectorAll(".work-filters li");
  const items = document.querySelectorAll(".portfolio-item");
  const loadMoreBtn = document.getElementById("loadMoreBtn");
  const grid = document.querySelector(".portfolio-grid");

  const isMobile = window.innerWidth <= 768;

  let activeFilter = "podcast";
  let visibleCount = isMobile ? 2 : 8;

  function initPopup() {
    $(grid).magnificPopup("destroy");

    $(grid).magnificPopup({
      delegate: '.portfolio-item:not([style*="display: none"]) a',
      type: 'image',
      gallery: {
        enabled: true
      },
      zoom: {
        enabled: true,
        duration: 300
      }
    });
  }

  function updateItems() {
    let filteredItems = [];

    items.forEach(item => {
      item.style.display = "none";
      if (item.classList.contains(activeFilter)) {
        filteredItems.push(item);
      }
    });

    filteredItems.forEach((item, index) => {
      if (index < visibleCount) {
        item.style.display = "block";
      }
    });

    loadMoreBtn.style.display =
      filteredItems.length > visibleCount ? "inline-block" : "none";

    initPopup();
  }

  filterButtons.forEach(btn => {
    btn.addEventListener("click", () => {
      filterButtons.forEach(b => b.classList.remove("active"));
      btn.classList.add("active");

      activeFilter = btn.dataset.filter;
      visibleCount = isMobile ? 2 : 8;
      updateItems();
    });
  });

  loadMoreBtn.addEventListener("click", () => {
    visibleCount += isMobile ? 2 : 4;
    updateItems();
  });

  updateItems();
});
</script>

